#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*ptr;
	int		x;

	ptr = lst;
	x = 0;
	while (ptr)
	{
		x++;
		ptr = ptr->next;
	}
	return (x);
}
