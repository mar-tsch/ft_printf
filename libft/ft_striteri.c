#include "../libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	x;

	if (s != 0 && f != 0)
	{
		x = 0;
		while (s[x] != '\0')
		{
			f(x, s + x);
			x++;
		}
	}
}
