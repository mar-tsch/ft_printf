#include "../libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*us1;
	unsigned char	*us2;
	size_t			x;

	us1 = (unsigned char *)s1;
	us2 = (unsigned char *)s2;
	x = 0;
	while (x < n)
	{
		if (us1[x] != us2[x])
			return (us1[x] - us2[x]);
		x++;
	}
	return (0);
}
